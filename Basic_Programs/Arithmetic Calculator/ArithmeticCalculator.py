number_1 = int(input("Enter the number1: "))
number_2 = int(input("Enter the number2: "))
operator = input("Enter the operator: ")

if operator == '+':
    print(f"The Sum of {number_1} and {number_2} is {number_1 + number_2}")
elif operator == '-':
    print(f"The Difference between {number_1} and {number_2} is {number_1 - number_2}")
elif operator == '*':
    print(f"The Product of {number_1} and {number_2} is {number_1 * number_2}")
elif operator == '/':
    print(f"The Division of {number_1} by {number_2} is {number_1 / number_2}")




